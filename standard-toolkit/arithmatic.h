int add(int a, int b)
{
    int final = a + b;
    return final;
}

int subtract(int a, int b)
{
    int final = a - b;
    return final;
}

int multiply(int a, int b)
{
    int final = a * b;
    return final;
}

int divide(int a, int b)
{
    int final = a / b;
    return final;
}

